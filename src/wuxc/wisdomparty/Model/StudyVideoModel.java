package wuxc.wisdomparty.Model;

public class StudyVideoModel {

	private String NumberCollect;
	private String NumberGreat;
	private String Time;
	private String Title;
	private String ImageUrl;
	private String url;
	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getNumberCollect() {
		return NumberCollect;
	}

	public void setNumberCollect(String numberCollect) {
		NumberCollect = numberCollect;
	}

	public String getNumberGreat() {
		return NumberGreat;
	}

	public void setNumberGreat(String numberGreat) {
		NumberGreat = numberGreat;
	}

	public String getTime() {
		return Time;
	}

	public void setTime(String time) {
		Time = time;
	}

	public String getTitle() {
		return Title;
	}

	public void setTitle(String title) {
		Title = title;
	}

	public String getImageUrl() {
		return ImageUrl;
	}

	public void setImageUrl(String imageUrl) {
		ImageUrl = imageUrl;
	}

}
