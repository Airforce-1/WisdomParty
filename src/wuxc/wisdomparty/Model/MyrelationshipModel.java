package wuxc.wisdomparty.Model;

public class MyrelationshipModel {
	private String out;
	private String time;
	private String in;
	private boolean IsPink;
	private String link;
	private boolean cont;

	public String getLink() {
		return link;
	}

	public void setLink(String link) {
		this.link = link;
	}

	public boolean isCont() {
		return cont;
	}

	public void setCont(boolean cont) {
		this.cont = cont;
	}
	public String getOut() {
		return out;
	}

	public void setOut(String out) {
		this.out = out;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getIn() {
		return in;
	}

	public void setIn(String in) {
		this.in = in;
	}

	public boolean isIsPink() {
		return IsPink;
	}

	public void setIsPink(boolean isPink) {
		IsPink = isPink;
	}

}
